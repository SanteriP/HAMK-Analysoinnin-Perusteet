number1 = 6
number2 = 5

if number1 == number2:
    print("number1 is equal to number2")

if number1 > number2:
    print("number1 is greater than number1")

if number1 >= number2:
    print("number1 is greater or equal to number2")

if number1 != number2:
    print("number1 is NOT equal to number2")